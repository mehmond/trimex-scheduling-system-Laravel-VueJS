    methods: {},

    async created() {
        const [userCount, subjectCount] = await Promise.all([
            this.callApi("get", "app/count_user"),
            this.callApi("get", "app/count_subject")
        ]);
        if (userCount.status === 200) {
            this.data.userCount = userCount.data;
        } else {
            if (userCount.status === 404) {
                this.error("No Records Found!");
            }
            this.swr();
        }
        if (subjectCount.status === 200) {
            this.data.subjectCount = subjectCount.data;
        } else {
            if (subjectCount.status === 404) {
                this.error("No Records Found!");
            }
            this.swr();
        }
    }
};
